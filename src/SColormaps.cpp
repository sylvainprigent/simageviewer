QImage SColormaps::getView(QString colormap, int height){

    int* map = this->getColorMap(colormap);

    QImage image(256, height, QImage::Format_RGB32);
    for (int x = 0 ; x < height ; x ++  ){
        for (int y = 0 ; y < 256 ; y ++  ){
            image.setPixel(y,x, qRgb(map[3*y + 0], map[3*y + 1], map[3*y + 2]) );
        }
    }
    return image;

}

